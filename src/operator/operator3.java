package operator;

public class operator3 {
    static void main(String[] args) {

        // 연산자 우선순위
        int sum1 = 2 * 2 + 3 * 3;
        int sum2 = (2 * 2) + (3 * 3);
        System.out.println(sum1);
        System.out.println(sum2);


        // 길이가 길어지더라도 코드를 단순하고 명확하게 적어야 유지보수에 유용하다.
        // 연산자 우선순위가 존재하지만 대부분 상식적인 순이다.
        // 가장 1순위는 괄호(), 가장 끝순위는 단순 연산자이다.

    }
}
